<h1>Creation</h1>

<div class='explanation'>
	<p>Making a new character in Apocalyptia is easy. Just follow these 6 steps:</p>
</div>

<div class='step'>
	<h3>Step 1: Description</h3>
	<p>Describe your Character's identity, age, sex, height, weight, skin, and hair.</p>
</div>

<div class='step'>
	<h3>Step 2: Traits</h3>
	<p>Distribute 14 Trait points between your Character's 4 Traits:</p>
	<p>Agility, Brains, Constitution, and Demeanor</p>
	<p>Trait scores range from 1 to 6.</p>
</div>

<div class='step'>
	<h3>Step 3: Skills</h3>
	<p>Distribute Brains x 3 Skill points between your Character's 16 Skills.</p>
	<p>The maximum possible score for each Skill is equal to the Skill's parent Trait score.</p>
	<p><span class='skill-type'>Agility Skills:</span></p>
	<p>Acrobatics, Larceny, Ranged, Stealth</p>
	<p><span class='skill-type'>Brains Skills:</span></p>
	<p>Medicine, Perception, Science, Survival</p>
	<p><span class='skill-type'>Constitution Skills:</span></p>
	<p>Athletics, Build, Drive, Melee</p>
	<p><span class='skill-type'>Demeanor Skills:</span></p>
	<p>Leadership, Perform, Socialize, Tame</p>
</div>

<div class='step'>
<h3>Step 4: Properties</h3>
<p>Your Character's Properties are calculated automatically:</p>
	<ul>
		<li>Speed = Agility x 3</li>
		<li>Experience = Brains x 3</li>
		<li>Health = Constitution x 3</li>
		<li>Psyche = Demeanor x 3</li>
		<li>Dodge = Acrobatics</li>
		<li>Intellect = Brains</li>
		<li>Block = Melee</li>
		<li>Luck = Demeanor</li>
	</ul>
</div>

<div class='step'>
	<h3>Step 5: Abilities</h3>
	<p>Buy Abilities for your Character using Experience Points (XP), or save some or all of your starting XP for later.</p>
</div>

<div class='step'>
	<h3>Step 6: Gear</h3>
	<p>Equip your Character with a random Melee weapon, a random Ranged weapon (with some random Ammo), and a random piece of Armor.</p>
</div>

