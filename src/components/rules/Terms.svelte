<div class="terms-rules">
    <h2>Symbols and Terms</h2>
    <div id="SymbolsSec" class="Sec2">
        <button id="DiceTermsBtn" class="Btn">DICE</button>
        <div id="DiceTermsSec" class="Sec3">
            <ul>
                <li>d6: One six-sided die. The only dice needed to play.</li>
                <li>DIFFICULTY (#): Target number you must beat to Succeed.</li>
                <li>EXPLODE (6): Rolling 6, rolling again, and adding up all rolls.</li>
                <li>SUCCESS (S): Rolling above #.</li>
                <li>FAIL (F): Rolling at or under #.</li>
                <li>BOTCH (1): Rolling 1, then re-rolling another 1. Very bad.</li>
                <li>MODIFIER: A bonus or penalty to rolls or Difficulties.</li>
                <li>RESULT: The total of a roll plus or minus any modifiers.</li>
            </ul>
        </div>
        <button id="GameTermsBtn" class="Btn">GAME</button>
        <div id="GameTermsSec" class="Sec3">
            <ul>
                <li>ABILITY: Character upgrades that are bought with Experience.</li>
                <li>ACTION: One significant thing a Character does in a round.</li>
                <li>AR: Armor Rating. Protection given by Armor.</li>
                <li>ATK: Attack. Can be Melee or Ranged. Roll vs Defense.</li>
                <li>BDMG: Blunt Damage. Does not cause Bleeding.</li>
                <li>CR: Cold-Resistant. Delays death from Hypothermia.</li>
                <li>DEF: Defense. Block, Dodge, or Reflex. Attack Difficulty.</li>
                <li>DMG: Damage. Reduces Health Points.</li>
                <li>DR: Damage Reduction. Protection given by Cover.</li>
                <li>EXTRAS: Non-Player Characters and creatures.</li>
                <li>FAST: An action that takes only a split second.</li>
                <li>FDMG: Fire Damage. Can be permanent.</li>
                <li>FR: Fire-Resistant. Protection against Fire Damage.</li>
                <li>GN: Game Narrator. The Player who directs the game.</li>
                <li>HP: Health Points. Maximum Damage Locations can take.</li>
                <li>LOCATION: Body parts: Head, Torso, Arms, or Legs.</li>
                <li>MANEUVER: Special kinds of actions Characters may perform.</li>
                <li>MATK or RATK: Melee Attack or Ranged Attack.</li>
                <li>MGL: Master Gear List. Table with every piece of Gear.</li>
                <li>PAIN: Penalty to all rolls from Damage and other sources.</li>
                <li>REFLEX: Passive Defense = Perception.</li>
                <li>RNG: Range incremented in yards.</li>
                <li>RND: Round. 3 seconds of in-game time, usually in combat.</li>
                <li>SCENARIO: The type of apocalypse taking place in your game.</li>
                <li>SITUATION: Circumstances or status effects that cause modifiers.</li>
                <li>SIZE (SZ): Measure of volume and weight. 1Sz ≈ 5lbs ≈ 1/2gal.</li>
            </ul>
        </div>
        <button id="TraitsTermsBtn" class="Btn">TRAITS</button>
        <div id="TraitsTermsSec" class="Sec3">
            <p>Agility, Brains, Constitution, and Demeanor. Rated 1 to 6.</p>
            <ul>
                <li>A: Agility Trait. A rolls are shown as A#.</li>
                <li>B: Brains Trait. B rolls are shown as B#.</li>
                <li>C: Constitution Trait. C rolls are shown as C#.</li>
                <li>D: Demeanor Trait. D rolls are shown as D#.</li>
            </ul>
        </div>
        <button id="InstinctsTermsBtn" class="Btn">INSTINCTS</button>
        <div id="InstinctsTermsSec" class="Sec3">
            <p>Natural talents everyone has based on Traits.</p>
            <ul>
                <li>ATHLETICS: = C. Climbing and swimming.</li>
                <li>PERCEPTION: = B. Processing sensory information.</li>
                <li>SOCIALIZE: = D. Interacting with others.</li>
                <li>STEALTH: = A. Remaining undetected.</li>
            </ul>
        </div>
        <button id="SkillsTermsBtn" class="Btn">SKILLS</button>
        <div id="SkillsTermsSec" class="Sec3">
            <p>12 different types of actions limited by their Traits.</p>
            <ul>
                <li>AGILITY SKILLS: Acrobatics, Ranged, Larceny</li>
                <li>BRAINS SKILLS: Medicine, Science, Survival</li>
                <li>CONSTITUTION SKILLS: Build, Drive, Melee</li>
                <li>DEMEANOR SKILLS: Entertain, Leadership, Tame</li>
                <li>Specialty: Sub-Skill that can change independently.</li>
            </ul>
        </div>
        <button id="PropertiesTermsBtn" class="Btn">PROPERTIES</button>
        <div id="PropertiesTermsSec" class="Sec3">
            <p>Trait-derived values.</p>
            <ul>
                <li>CARRY = [C x6]. Maximum total Size you can carry.</li>
                <li>DMG MOD = Melee and Ranged(Thrown) Damage modifier.</li>
                <li>LUCK = D /day. Spend for various effects.</li>
                <li>PSYCHE = [D]. Self-tracked mental health.</li>
                <li>SPEED = [A + C] yds. Jog = Speed x2.</li>
                <li>THRESHOLD = [C + D]. Pain you can take before passing out.</li>
                <li>XP = B x6, then B each in-game week.</li>
            </ul>
        </div>
    </div>
</div>