<div id="DiseasesSec" class="Sec2">
    <p>Below is a short list of diseases that are likely to appear as major plagues in an Apocalyptia game.</p>
    <p>Transmission is the method by which the disease spreads. Roll Constitution vs Virulence to avoid infection when exposed.</p>
    <p>An attendant caring for a patient must roll Medicine vs the disease’s Diagnose to figure out if there is an effective Treatment and how to apply it. Attendants must roll Luck vs Contagion to avoid exposing themselves to some Diseases while Treating their patients. If their Luck roll Fails, they have been exposed and must roll Constitution vs Virulence to avoid infection. Diseases are considered “toxins” for the purposes of GEAR bonuses.</p>
    <button id="CholeraDiseasesBtn" class="Btn">CHOLERA</button>
    <div id="CholeraDiseasesSec" class="Sec3">
        <p>Transmission: Food/Water</p>
        <p>Virulence: 9#</p>
        <p>Diagnose: 9#</p>
        <p>Onset: d6 hours</p>
        <p>Duration: d6x3 days</p>
        <p>Symptoms: abdominal pain, nausea, vomiting, diarrhea, dehydration</p>
        <p>Effect: Dehydration requires 6 rations of Water/day. 1 Pain/day.</p>
        <p>Treatment: Dehydration requires 6 rations of purified Water/day to avoid death. Antibiotics reduces Duration by 1 day per dose.</p>
    </div>
    <button id="HemorrhagicFeverDiseasesBtn" class="Btn">HEMORRHAGIC FEVER</button>
    <div id="HemorrhagicFeverDiseasesSec" class="Sec3">
        <p>Transmission: Body Fluids</p>
        <p>Virulence: 12#</p>
        <p>Diagnose: 12#</p>
        <p>Onset: d6x3 days</p>
        <p>Duration: d6 weeks</p>
        <p>Symptoms: fever, fatigue, aches, sore throat, vomiting, diarrhea, rash, impaired kidney/liver function, internal/external bleeding</p>
        <p>Effect: 1 Pain/day. Roll C6# 1/day to avoid 1 Torso DMG. Natural Recovery is halted during infection.</p>
        <p>Treatment: Medicine 9# 1/day to grant +1C to resist DMG. Contagion: Luck 6# 1/day.</p>
    </div>
    <button id="InfluenzaDiseasesBtn" class="Btn">INFLUENZA</button>
    <div id="InfluenzaDiseasesSec" class="Sec3">
        <p>Transmission: Air, 10yds</p>
        <p>Virulence: 9#</p>
        <p>Diagnose: 6#</p>
        <p>Onset: d6 days</p>
        <p>Duration: d6x2 days</p>
        <p>Symptoms: fever, headache, fatigue, vomiting, sore throat, sneezing</p>
        <p>Effect: 1 Exhaustion/day. Roll C6# 1/day to avoid 1 Torso DMG.</p>
        <p>Treatment: Medicine 6# 1/day to grant +1C to resist DMG. Contagion: Luck 15# 1/day.</p>
    </div>
    <button id="MRSADiseasesBtn" class="Btn">MRSA</button>
    <div id="MRSADiseasesSec" class="Sec3">
        <p>Transmission: Touch</p>
        <p>Virulence: 12#</p>
        <p>Diagnose: 18#</p>
        <p>Onset: d6 weeks</p>
        <p>Duration: d6 days</p>
        <p>Symptoms: small red bumps on skin, fever, rash, puss-filled boils</p>
        <p>Effect: MRSA can live on surfaces for d6 days after contact. Roll C9# every 12hrs to avoid 1 Torso DMG while infection lasts. Natural Recovery is halted during infection.</p>
        <p>Treatment: No effective Treatment. No response to Antibiotics. Contagion: Luck 9# 1/day.</p>
    </div>
    <button id="RabiesDiseasesBtn" class="Btn">RABIES</button>
    <div id="RabiesDiseasesSec" class="Sec3">
        <p>Transmission: Body fluids</p>
        <p>Virulence: 15#</p>
        <p>Diagnose: 6#</p>
        <p>Onset: d6 weeks</p>
        <p>Duration: d6x4 days</p>
        <p>Symptoms: fever, aches, hydrophobia, low blood pressure, sweating, vomiting, drooling foam, convulsions, photophobia, pupil dilation, agitation, dehydration, fatigue, delirium, paralysis, stupor, coma</p>
        <p>Effect: Take 1 Head DMG every day while infection lasts. Natural Recovery is halted during infection.</p>
        <p>Treatment: Amputation of exposed Location within d6 minutes prevents infection of the entire body. No further Treatment. Contagion: Luck 3# 1/day.</p>
    </div>
