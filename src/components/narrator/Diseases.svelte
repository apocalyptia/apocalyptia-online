    <div id="SmallpoxDiseasesSec" class="Sec3">
        <p>Transmission: Air, 3yds</p>
        <p>Virulence: 12#</p>
        <p>Diagnose: 3#</p>
        <p>Onset: d6x2 days</p>
        <p>Duration: d6x3 days</p>
        <p>Symptoms: headache, fever, fatigue, nausea, muscle pain, lesions in mouth, lesions on face and body, vomiting, rash, blackened skin</p>
        <p>Effect: Smallpox can live for d6x3 days in infected linens. Roll C9# 1/day to avoid 1 Torso DMG while infection lasts. Natural Recovery is halted during infection.</p>
        <p>Treatment: Medicine 6# 1/day to grant +1C to resist DMG. Contagion: Luck 12# 1/day.</p>
    </div>
