        <div id="AreasEncountersSec" class="Sec2">
            <p>Apocalyptia is designed to be easy to run, especially for a first-time GN. Using the AREA and ENCOUNTER tables provide a way to randomly generate a dynamic game world. This section is meant to be a supplement to the story, not a replacement for it. The GN can use this section to maintain a steady pace of game play and to spark new ideas if the game session becomes stale and boring. The GN can save game time by rolling a list of random AREAS and/or ENCOUNTERS before the session begins.</p>
            <p>The two basic types of AREAS the Team may wander through in the Wasteland are RURAL and URBAN. RURAL encompasses small towns, farmlands, and the surrounding countryside. These places are usually a little safer but the looting is sparse. URBAN includes downtown business districts, industrial sprawl, and suburban neighborhoods. These places are full of loot but tend to be much more dangerous and harder to escape.</p>
            <p>AREAS can be rolled whenever the Team takes off in an unexplored direction. The GN may roll even/odd on d6 to determine if there is an ENCOUNTER waiting at the new AREA. ENCOUNTERS could be rolled at least once per day, even when the Team is not traveling. SCENARIO ENCOUNTER tables are given at the end of this section and at the end of the chapter under the Scenario that you chose for your game. All Extras are listed in Ch:5.</p>
