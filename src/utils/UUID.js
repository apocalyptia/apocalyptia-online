export default _ => {
	const characters = 'abcdefghijklmnopqrstuvwzyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789'
	let uuid = '??????-??????-??????-??????-??????-??????'
	uuid = uuid.split('')
	for (let i = 0; i < uuid.length; i++) {
		if (uuid[i] == '?') {
			uuid[i] = characters[Math.floor(Math.random() * characters.length)]
		}
	}
	uuid = uuid.join('')
	return uuid
}